function assertBandNamesAreSame(expected, test) {
  function pass() { return '' }
  
  function fail() {
    function bandNamesToString() {
      return test.bandNames().iterate(function(s, prev) { 
        return ee.String(prev).cat(ee.String(s)).cat(', ') }, ee.String('')
      )
    }
      return ee.String('Error: band names are not same for ').cat(test.get('LANDSAT_SCENE_ID'))
        .cat('\nBand names: ').cat(bandNamesToString())
  }

  print(ee.Algorithms.If(expected.bandNames().equals(test.bandNames()), pass(), fail()))
}

function testScene(collectionName, testId) {
  var l3 = new ee.ImageCollection(collectionName)
  
  var firstImage = ee.Image(l3.first())
  var firstId = firstImage.get('LANDSAT_SCENE_ID')

  var testImage = ee.Image(testId)

  assertBandNamesAreSame(firstImage, testImage)

  // add as a filtered image collection - fails
  var filteredCollection = l3.filter(ee.Filter.inList('LANDSAT_SCENE_ID', [firstId, testId]))
  Map.addLayer(filteredCollection, {}, testId)
}

